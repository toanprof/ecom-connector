console.log('   - Or use Method 3 (Manual Seller Center)\n');

console.log('If you need pc_sign:');
console.log('   - pc_sign is generated by TikTok Partner Portal');
console.log('   - It\'s provided when you create a service');
console.log('   - Alternative: use auth code flow (Method 1)\n');

console.log('Common Issues:');
