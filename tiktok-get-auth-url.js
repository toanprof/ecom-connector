require('dotenv').config();

console.log('\n========================================');
console.log('   TikTok Shop - Get Authorization URL');
console.log('========================================\n');

const appKey = process.env.TIKTOK_APP_KEY;
const appSecret = process.env.TIKTOK_APP_SECRET;
const redirectUrl = 'https://hdseafood.1erp.vn/';

if (!appKey || !appSecret) {
  console.error('❌ Missing credentials in .env!');
  console.log('Required: TIKTOK_APP_KEY, TIKTOK_APP_SECRET');
  process.exit(1);
}

console.log('📋 Configuration:');
console.log('   App Key:', appKey);
console.log('   App Secret:', appSecret ? '****' + appSecret.slice(-8) : 'Not set');
console.log('   Redirect URL:', redirectUrl);
console.log();

console.log('═══════════════════════════════════════');
console.log('🔗 Authorization URLs');
console.log('═══════════════════════════════════════\n');

// Method 1: Using Auth API v2 (Simplest - No pc_sign required)
console.log('✅ Method 1: Auth API v2 (Recommended - Simplest)\n');
console.log('This method uses auth code flow without pc_sign\n');

const authUrlV2 = `https://auth.tiktok-shops.com/oauth/authorize?app_key=${appKey}&state=your_state_value&redirect_uri=${encodeURIComponent(redirectUrl)}`;

console.log('Authorization URL:');
console.log(authUrlV2);
console.log();

console.log('Steps:');
console.log('1. Open this URL in browser');
console.log('2. Login to TikTok Shop seller account');
console.log('3. Authorize the app');
console.log('4. You will be redirected to:');
console.log(`   ${redirectUrl}?code=<auth_code>&state=your_state_value`);
console.log();

// Method 2: TikTok Services (if you have service_id)
console.log('─────────────────────────────────────────\n');
console.log('✅ Method 2: TikTok Services (If you have Service ID)\n');
console.log('This requires registering your app in TikTok Partner Portal\n');
console.log('URL: https://services.tiktokshop.com/open/authorize?service_id=<your_service_id>');
console.log();
console.log('Note: service_id is obtained from TikTok Partner Portal after creating your service');
console.log();

// Method 3: Direct seller authorization (requires navigation)
console.log('─────────────────────────────────────────\n');
console.log('✅ Method 3: Manual Seller Center Authorization\n');
console.log('If other methods don\'t work, manually authorize in Seller Center:\n');
console.log('1. Login to: https://seller-vn.tiktok.com/');
console.log('2. Go to: Settings > Open Platform');
console.log('3. Find your app by App Key: ' + appKey);
console.log('4. Click "Authorize" button');
console.log('5. System will redirect to your callback URL with auth_code');
console.log();

console.log('═══════════════════════════════════════');
console.log('📝 Next Steps');
console.log('═══════════════════════════════════════\n');

console.log('1️⃣  Copy and open the URL above in browser');
console.log('2️⃣  Login and authorize your TikTok Shop');
console.log('3️⃣  After redirect, copy the auth_code from URL');
console.log('4️⃣  Use the auth_code to get access token:\n');

console.log('   Example callback URL:');
console.log(`   ${redirectUrl}?code=ABC123XYZ789DEF456GHI\n`);

console.log('5️⃣  Run the get-token script:');
console.log('   node tiktok-get-token.js ABC123XYZ789DEF456GHI\n');

